-- -----------------------------------------------------------------------------
-- 발송 이력
-- 실제 메일 발송 엔진이 아니라도 "무엇을 누구에게 보냈는지"를 추적할 수 있게 한다.
-- -----------------------------------------------------------------------------
create table if not exists subscription_dispatch_histories (
    id bigint generated by default as identity primary key,
    content_type varchar(40) not null,
    content_slug varchar(160) not null,
    content_title varchar(255) not null,
    content_summary varchar(1000),
    dispatch_channel varchar(20) not null,
    dispatch_status varchar(20) not null,
    recipient_count bigint not null,
    content_created_at timestamp,
    dispatched_at timestamp not null,
    created_at timestamp not null default current_timestamp,
    updated_at timestamp not null default current_timestamp,
    constraint uk_subscription_dispatch_content unique (content_type, content_slug, dispatch_channel)
);

create index if not exists idx_email_subscriptions_active on email_subscriptions(active);
create index if not exists idx_subscription_dispatch_histories_dispatched_at
    on subscription_dispatch_histories(dispatched_at);

comment on table subscription_dispatch_histories is '구독 콘텐츠 발송 이력';
