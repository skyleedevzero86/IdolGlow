-- =============================================================================
-- V17 (PostgreSQL) - 운영 보정용 누락 테이블 복구
-- =============================================================================
-- 배경
--   일부 PostgreSQL 환경은 flyway_schema_history 상으로는 V16까지 반영된 것처럼
--   보이지만, 실제 운영 보조 테이블(admin/webzine/newsletter/subscription)이
--   누락된 상태일 수 있다.
--
-- 목적
--   1. 누락된 후속 관리 테이블을 안전하게 복구한다.
--   2. 이미 존재하는 테이블과 충돌하지 않도록 모두 IF NOT EXISTS로 생성한다.
--   3. 복구 마이그레이션은 "구조 복원"에 집중하고, 강한 FK는 후속 마이그레이션에서
--      점진적으로 보강할 수 있게 한다.
--
-- 주의
--   admin_audit_logs.admin_user_id는 의도적으로 users FK를 두지 않는다.
--   운영 DB가 부분 동기화 상태일 수 있어, 이 단계에서는 부팅 가능 상태를 먼저
--   복원하고 이후 정합성 마이그레이션에서 FK를 추가하는 전략을 택한다.
-- =============================================================================

-- -----------------------------------------------------------------------------
-- 관리자 감사 로그
-- 운영 추적성 확보를 위한 핵심 테이블. 관리자 행위 이력의 최소 단위를 보존한다.
-- -----------------------------------------------------------------------------
create table if not exists admin_audit_logs (
    id bigint generated always as identity primary key,
    admin_user_id bigint not null,
    action_code varchar(64) not null,
    target_type varchar(64) not null,
    target_id bigint,
    detail varchar(2000),
    created_at timestamp(6) not null
);

create index if not exists idx_admin_audit_logs_created_at on admin_audit_logs (created_at desc);
create index if not exists idx_admin_audit_logs_admin_user on admin_audit_logs (admin_user_id);

comment on table admin_audit_logs is '관리자 주요 행위에 대한 감사 로그';
comment on column admin_audit_logs.id is '감사 로그 PK';
comment on column admin_audit_logs.admin_user_id is '행위를 수행한 관리자 식별자. 운영 복구 단계에서는 FK를 지연 적용한다';
comment on column admin_audit_logs.action_code is '행위 코드. 예: RESERVATION_CANCEL, ISSUE_CREATE';
comment on column admin_audit_logs.target_type is '대상 도메인 유형. 예: RESERVATION, WEBZINE_ISSUE';
comment on column admin_audit_logs.target_id is '대상 엔티티 식별자';
comment on column admin_audit_logs.detail is '추가 설명 또는 JSON 스냅샷';
comment on column admin_audit_logs.created_at is '감사 로그 생성 시각';
comment on index idx_admin_audit_logs_created_at is '최신 감사 로그 목록 조회 최적화';
comment on index idx_admin_audit_logs_admin_user is '관리자별 감사 로그 조회 최적화';

-- -----------------------------------------------------------------------------
-- 웹진 호
-- 호 단위 메타데이터를 보관하며, 기사의 루트 aggregate 역할을 한다.
-- -----------------------------------------------------------------------------
create table if not exists webzine_issues (
    id bigint generated by default as identity primary key,
    slug varchar(80) not null,
    volume integer not null,
    issue_date date not null,
    cover_image_url varchar(500) not null,
    teaser varchar(1000) not null,
    created_at timestamp not null default current_timestamp,
    updated_at timestamp not null default current_timestamp,
    constraint uk_webzine_issue_slug unique (slug),
    constraint uk_webzine_issue_volume unique (volume)
);

comment on table webzine_issues is '웹진 호 메타데이터';
comment on column webzine_issues.slug is '호 공개 slug';
comment on column webzine_issues.volume is '발행 호수';
comment on column webzine_issues.issue_date is '발행일';
comment on column webzine_issues.cover_image_url is '호 표지 이미지 URL';
comment on column webzine_issues.teaser is '호 소개 문구';

-- -----------------------------------------------------------------------------
-- 웹진 기사
-- 호에 속한 개별 기사. 카드/상세 렌더링에 필요한 핵심 메타를 가진다.
-- -----------------------------------------------------------------------------
create table if not exists webzine_articles (
    id bigint generated by default as identity primary key,
    issue_id bigint not null references webzine_issues(id) on delete cascade,
    slug varchar(150) not null,
    title varchar(200) not null,
    kicker varchar(200) not null,
    summary text not null,
    hero_image_url varchar(500) not null,
    card_image_url varchar(500) not null,
    category varchar(30) not null,
    format_label varchar(60) not null,
    author_name varchar(120) not null,
    author_email varchar(255) not null,
    credit_line varchar(255) not null,
    highlight_quote varchar(500),
    created_at timestamp not null default current_timestamp,
    updated_at timestamp not null default current_timestamp,
    constraint uk_webzine_article_issue_slug unique (issue_id, slug)
);

create index if not exists idx_webzine_articles_issue_id on webzine_articles(issue_id);

comment on table webzine_articles is '웹진 기사 메타데이터';
comment on column webzine_articles.issue_id is '소속 웹진 호 FK';
comment on column webzine_articles.slug is '기사 공개 slug';
comment on column webzine_articles.title is '기사 제목';
