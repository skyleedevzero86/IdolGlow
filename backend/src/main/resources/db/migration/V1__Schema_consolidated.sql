-- 계정 검증용 일회성 토큰
-- 이메일 가입 인증이나 계정 확인에 쓰는 토큰을 저장한다.
-- 인덱스는 토큰 단건 조회와 이메일·유형·상태별 최신 행 조회에 맞춘다.

create table if not exists signup_verification_tokens (
    id             bigint        not null generated by default as identity primary key,
    token          varchar(120)  not null, -- 일회성 검증 토큰(전역 유일)
    type           varchar(40)   not null, -- 검증 유형 코드
    email          varchar(190)  not null, -- 대상 이메일
    username       varchar(120),           -- 닉네임 등(선택)
    user_id        bigint,                 -- 확인 후 연결된 users.id(선택)
    status         varchar(30)   not null, -- 토큰 상태 코드
    requested_ip   varchar(64)   not null, -- 발급 요청 IP
    confirmed_ip   varchar(64),            -- 검증 완료 시 IP(선택)
    expires_at     timestamp(6)  not null, -- 만료 시각
    created_at     timestamp(6)  not null, -- 행 생성 시각
    confirmed_at   timestamp(6),           -- 검증·사용 완료 시각(선택)
    detail         varchar(500),           -- 거절 사유 등(선택)
    constraint uk_signup_verification_tokens_token unique (token)
);

create index if not exists idx_signup_verification_tokens_email_type_status_created
    on signup_verification_tokens (email, type, status, created_at desc);

comment on table signup_verification_tokens is '가입·계정 확인용 검증 토큰 저장소';
